public class loop {
    public static void main(String[] args){

//for loop
        for(int i = 1 ;i<=5;i++){
            System.out.print("Hello num: ");
            System.out.println(i);
        }
//while loop
        int tc = 1;
        while(tc <= 10){
            System.out.println(tc);
            tc++;
        }

    }
}
